export function getTranslatedMaterial(material: string): string {
    const key = material.toUpperCase();
    return MATERIAL_TRANSLATIONS[key] || formatStartCase(material);
}

export function getTranslatedEnchantment(enchantment: string): string {
    // Enchantments often come as "Sharpness V". We need to split if possible.
    const parts = enchantment.split(" ");
    const name = parts.slice(0, parts.length - 1).join(" ").toUpperCase().replace(/ /g, "_");
    const level = parts[parts.length - 1];

    // Check if the last part is actually a roman numeral or number. If not, treat whole string as name
    if (!isRomanOrNumber(level)) {
        const key = enchantment.toUpperCase().replace(/ /g, "_");
        return ENCHANTMENT_TRANSLATIONS[key] || formatStartCase(enchantment);
    }

    const translatedName = ENCHANTMENT_TRANSLATIONS[name] || formatStartCase(name);
    return `${translatedName} ${level}`;
}

function isRomanOrNumber(s: string): boolean {
    return /^M{0,4}(CM|CD|D?C{0,3})(XC|XL|L?X{0,3})(IX|IV|V?I{0,3})$/.test(s) || /^\d+$/.test(s);
}

function formatStartCase(text: string): string {
    return text
        .toLowerCase()
        .replace(/_/g, ' ')
        .split(' ')
        .map(word => word.charAt(0).toUpperCase() + word.slice(1))
        .join(' ');
}

export function getItemIconUrl(material: string): string {
    // Using a reliable CDN for Minecraft assets
    // Converting to lower case and replacing generic names if needed
    let mat = material.toLowerCase();

    // Handle some inconsistencies or legacy names if necessary
    if (mat === 'cobweb') mat = 'web'; // Example fix if needed, but 'cobweb' usually works on some CDNs

    return `https://assets.mcasset.cloud/1.20.1/assets/minecraft/textures/item/${mat}.png`;
}

export function getBlockIconUrl(material: string): string {
    // Fallback for blocks if item texture doesn't exist (less common for inventory items but possible)
    let mat = material.toLowerCase();
    return `https://assets.mcasset.cloud/1.20.1/assets/minecraft/textures/block/${mat}.png`;
}
